def decodeMorse(morse_code):
    morse_code = morse_code.strip().split('   ')
    print(morse_code)
    result = ''
    for item in morse_code:
        for word in item.split(' '):
            result += MORSE_CODE[word]
        result += ' '
    return result.strip(" ")


